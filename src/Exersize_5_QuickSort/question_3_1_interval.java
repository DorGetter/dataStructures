package Exersize_5_QuickSort;

public class question_3_1_interval {
//interval class// 
	public static class Node<AnyType>
	{
	   public double x;
	   public double y;
	   

	   public Node(double x, double y)
	   {
	      this.x = x;
	      this.y = y;
	   }
	   public Node() {}


	public double getX() {
		return x;
	}


	public void setX(double x) {
		this.x = x;
	}


	public double getY() {
		return y;
	}


	public void setY(double y) {
		this.y = y;
	}
	   
	   
	}





}
